export type ProductSlug = (typeof displayOrder)[number];

/** Look up the slugs immediately before and after `slug` in `displayOrder`. */
export function getNeighborSlugs(slug: string): { prev: string | null; next: string | null } {
  const i = displayOrder.indexOf(slug as ProductSlug);
  if (i === -1) return { prev: null, next: null };
  return {
    prev: i > 0 ? displayOrder[i - 1] : null,
    next: i < displayOrder.length - 1 ? displayOrder[i + 1] : null,
  };
}
